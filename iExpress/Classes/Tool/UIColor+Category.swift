import UIKit

extension UIColor {
    
    class func lightGreenColor() -> UIColor {
        return UIColor(red: 20/255, green: 160/255, blue: 160/255, alpha: 1)
    }
    
    class func smallLightGrayColor() -> UIColor {
        return UIColor(red: 230/255, green: 230/255, blue: 230/255, alpha: 1)
    }
    
    class func darkWhiteColor() -> UIColor {
        return UIColor(red: 245/255, green: 245/255, blue: 245/255, alpha: 1)
    }

    class func appBlueColor() -> UIColor {
        return UIColor(red: 0/255, green: 112/255, blue: 232/255, alpha: 1)
    }
}
